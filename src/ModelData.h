#ifndef MODELDATA_H
#define MODELDATA_H

#include "stdgl.h"

/***************************************************************************//**
 * Storage class for model data.
 * Holds raw data. To be filled by other tools for loading models from files or
 * other sources.
 ******************************************************************************/
class ModelData
{
public:
    /***********************************************************************//**
     * Constructor.
     **************************************************************************/
    ModelData();

    /***********************************************************************//**
     * Destructor.
     **************************************************************************/
    ~ModelData();

    /***********************************************************************//**
     * Holds the type of faces used in the model.
     **************************************************************************/
    GLuint vertexType;

    /***********************************************************************//**
     * Holds the number of vertices in the complete model.
     **************************************************************************/
    GLsizei vertexCount;

    /***********************************************************************//**
     * Tracks the number of textures.
     **************************************************************************/    
    int textureCount;

    /***********************************************************************//**
     * Holds texture image information on the GPU.
     **************************************************************************/    
    GLuint *textures;

    /***********************************************************************//**
     * Holds the vertex data for the model.
     **************************************************************************/
    float *vertexData;

    /***********************************************************************//**
     * Holds the vertex data for the model on the GPU.
     **************************************************************************/
    GLuint vertexBuffer;
};

#endif
